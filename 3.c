//Most Used Variables

#include<stdio.h>

int main(){
    
    //Int Variable is stored only Numbers
    int number = 100;

    //Float Variable is stored only decimal Values Then After Point Stored in only 6 digits
    float decimal = 99.990979;

    //Double Variable is stored only decimal Values Then After Point Stored in More Than 6 digits
    double decmial_2 = 89.07585723;

    //Char is a take Any Values Only For Between Strings
    char Name = 'A';

    //Value Print Code
    printf("%d \n%f\n%f\n%2lc",number,decimal,decmial_2,Name);

    return 0;
}
